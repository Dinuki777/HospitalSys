    private void btnAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAllActionPerformed
        // TODO add your handling code here:
        tblModel.setRowCount(0);
        ArrayList<Booking> bookingList=pDB.getAll();
        for(Booking p: bookingList){
            int bookingID=p.getBookingID();
           int patientID=p.getPatientID();
           int consultantID=p.getConsultantID();
           int room=p.getRoom();
           String time=p.getTime();
           String paid=p.getPaid();
           Date bookingDate=(Date) p.getBookingDate();
           
           tblModel.addRow(new Object[]{bookingID,patientID,consultantID,bookingDate,time,room,paid});
        }
    }//GEN-LAST:event_btnAllActionPerformed

