    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        int bookingID=Integer.valueOf(txtBID.getText());
        int patientID=Integer.valueOf(txtPID.getText());
        int consultantID=Integer.valueOf(txtCID.getText());
        int room=Integer.valueOf(txtroom.getText());
        String time=txtTime.getText();
        String paid=txtPaid.getText();
        Date bookingDate=Date.valueOf(txtBD.getText());

        Booking p=new Booking(bookingID,patientID,consultantID,bookingDate,time,room,paid);
        boolean flag=pDB.add(p);
        if(flag){
            JOptionPane.showMessageDialog(pnlBooking, "Correctly Add ");
        }else{
            JOptionPane.showMessageDialog(pnlBooking, "Correctly not Added");
         }
    }//GEN-LAST:event_btnAddActionPerformed

